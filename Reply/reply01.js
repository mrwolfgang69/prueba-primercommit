// // ** Encuentra la última letra de un string **

// La función findLastLetter() recibe como parámetro un string cualquiera, tu labor es tomar ese string y obtener el último caracter y retornarlo.

// Ejemplo: Si el string que recibe es "Hola, me llamo Erik", deberás retornar la letra k


function findLastLetter(text) {
    
    return text[text.length - 1]
  }
  